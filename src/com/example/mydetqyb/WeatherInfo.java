package com.example.mydetqyb;

public class WeatherInfo {
	private int id;
	private String minz;
	private String tianq;
	private String wend;
	private String pm;
	private String fong;
	private String shid;
	private String jiany;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getMinz() {
		return minz;
	}

	public void setMinz(String minz) {
		this.minz = minz;
	}

	public String getTianq() {
		return tianq;
	}

	public void setTianq(String tianq) {
		this.tianq = tianq;
	}

	public String getWend() {
		return wend;
	}

	public void setWend(String wend) {
		this.wend = wend;
	}

	public String getPm() {
		return pm;
	}

	public void setPm(String pm) {
		this.pm = pm;
	}

	public String getFong() {
		return fong;
	}

	public void setFong(String fong) {
		this.fong = fong;
	}
	public String getShid() {
		return shid;
	}

	public void setShid(String shid) {
		this.shid = shid;
	}
	public String getJiany() {
		return jiany;
	}

	public void setJiany(String jiany) {
		this.jiany = jiany;
	}
}
